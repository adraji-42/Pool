void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	save;

	i = 0;
	j = size - 1;
	while (i < j)
	{
		save = tab[i];
		tab[i] = tab[j];
		tab[j] = save;
		i++;
		j --;
	}
}
